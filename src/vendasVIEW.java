    private void listarProutos(){
        try {
            ProdutosDAO produtosDAO = new ProdutosDAO();
            
            DefaultTableModel model = (DefaultTableModel) jtListaProdutosVendidos.getModel();
            model.setNumRows(0);
            
            List<ProdutosDTO> listagem = produtosDAO.listarProdutoVendido();
            
            for(int i = 0; i < listagem.size(); i++){
                model.addRow(new Object[]{
                    listagem.get(i).getId(),
                    listagem.get(i).getNome(),
                    listagem.get(i).getValor(),
                    listagem.get(i).getStatus()
                });
                
            }    
        } catch (Exception e) {
                System.out.println("Erro ao Listar o Produto: " + e.getMessage());
            }
        }

